const NumberInput = React.forwardRef(function CustomNumberInput(props, ref) {
  return (
    <BaseNumberInput
      slots={{
        root: StyledInputRoot,
        input: StyledInputElement,
        incrementButton: StyledButton,
        decrementButton: StyledButton,
      }}
      slotProps={{
        incrementButton: {
          children: '▴',
        },
        decrementButton: {
          children: '▾',
        },
      }}
      {...props}
      ref={ref}
    />
  );
});

function F_Page_3() {
    const [value, setValue] = React.useState(null);
    const location = useLocation();
    const { totalCost } = location.state || {};
    const [message, setMessage] = useState('');

    const handleDivision = () => {
        console.log('Total Cost:', totalCost);
        console.log('Value:', value);
        if (value !== null && value >= 0 && !isNaN(value) && !isNaN(totalCost)) {
            const divisionResult = totalCost / value;
            if (!isNaN(divisionResult) && isFinite(divisionResult)) {
                setMessage(divisionResult.toFixed(2)); 
            } else {
                setMessage('Error: Division result is not a valid number');
            }
        } else {
            setMessage('Error: Invalid input value');
        }
    };

    return (
        <Stack sx={{flexDirection: "column", spacing: "10", alignItems: "center", justifyContent: "space-between"}}>
            <NumberInput
            aria-label="Number input"
            placeholder="Type a number…"
            value={value}
            onChange={(event, val) => setValue(val)}
            />
            <Button variant='contained' paddingTop='100' onClick={handleDivision}>Calculate Division</Button>
            <p>{message}</p>
        </Stack>
    );
}

